#!/usr/bin/env node
/**
 * Writes www/af-qa-env.js from project-root .env so the Cordova webview can read DEV_KEY / APP_ID
 * without bundling secrets into source. Runs on **before_prepare** so the file exists before www is
 * copied into native projects (after_prepare would be too late for packaged assets).
 *
 * .env format: KEY=value lines (DEV_KEY, APP_ID). Lines starting with # ignored.
 */
const fs = require('fs');
const path = require('path');

module.exports = function (context) {
  const root = context.opts.projectRoot;
  const envPath = path.join(root, '.env');
  const outPath = path.join(root, 'www', 'af-qa-env.js');
  let devKey = '';
  let appId = '';

  if (fs.existsSync(envPath)) {
    const content = fs.readFileSync(envPath, 'utf8');
    for (const line of content.split(/\r?\n/)) {
      const trimmed = line.trim();
      if (!trimmed || trimmed.startsWith('#')) continue;
      const eq = trimmed.indexOf('=');
      if (eq === -1) continue;
      const key = trimmed.slice(0, eq).trim();
      const val = trimmed.slice(eq + 1).trim();
      if (key === 'DEV_KEY') devKey = val;
      if (key === 'APP_ID') appId = val;
    }
  }

  const payload = { DEV_KEY: devKey, APP_ID: appId };
  const body =
    "'use strict';\n" +
    '// Generated by hooks/emit-af-qa-env.js from .env — do not edit.\n' +
    `window.__AF_QA_ENV__ = ${JSON.stringify(payload)};\n`;
  fs.writeFileSync(outPath, body, 'utf8');
};
